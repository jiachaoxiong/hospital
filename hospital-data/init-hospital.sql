-- ==========================================
-- hospital_hospital 数据库初始化脚本
-- ==========================================
CREATE TABLE IF NOT EXISTS `hospital` (
    `id` BIGINT AUTO_INCREMENT PRIMARY KEY,
    `name` VARCHAR(100) NOT NULL COMMENT '医院名称',
    `level` VARCHAR(20) DEFAULT '' COMMENT '等级',
    `province` VARCHAR(20) DEFAULT '' COMMENT '省份',
    `city` VARCHAR(20) DEFAULT '' COMMENT '城市',
    `address` VARCHAR(255) DEFAULT '' COMMENT '地址',
    `phone` VARCHAR(20) DEFAULT '' COMMENT '电话',
    `description` TEXT COMMENT '简介',
    `image_url` VARCHAR(255) DEFAULT '' COMMENT '图片URL',
    `create_time` DATETIME DEFAULT CURRENT_TIMESTAMP,
    `update_time` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    `deleted` TINYINT DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='医院表';

CREATE TABLE IF NOT EXISTS `department` (
    `id` BIGINT AUTO_INCREMENT PRIMARY KEY,
    `hospital_id` BIGINT NOT NULL COMMENT '所属医院ID',
    `name` VARCHAR(50) NOT NULL COMMENT '科室名称',
    `description` VARCHAR(500) DEFAULT '' COMMENT '简介',
    `create_time` DATETIME DEFAULT CURRENT_TIMESTAMP,
    `update_time` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    `deleted` TINYINT DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='科室表';

CREATE TABLE IF NOT EXISTS `doctor` (
    `id` BIGINT AUTO_INCREMENT PRIMARY KEY,
    `department_id` BIGINT NOT NULL COMMENT '所属科室ID',
    `hospital_id` BIGINT NOT NULL COMMENT '所属医院ID',
    `name` VARCHAR(20) NOT NULL COMMENT '姓名',
    `title` VARCHAR(20) DEFAULT '' COMMENT '职称',
    `description` VARCHAR(500) DEFAULT '' COMMENT '简介',
    `avatar_url` VARCHAR(255) DEFAULT '' COMMENT '头像',
    `create_time` DATETIME DEFAULT CURRENT_TIMESTAMP,
    `update_time` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    `deleted` TINYINT DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='医生表';

